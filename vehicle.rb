
class Vehicle
    def initialize(make, model, year, color, milage)
    @make=make
    @model=model
    @year=year
    @color=color
    @milage=milage
    end	



    def make
        @make
    def model
        @model
    def year
        @year
    def color
        @color
    def milage
        @milage
    	
    end




    def milage_change(x)
        @milage +=x


    end
